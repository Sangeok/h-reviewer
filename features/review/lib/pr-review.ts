import { createOctokitClient } from "@/lib/github/github";
import type { CodeSuggestion, StructuredIssue, RepeatBadgeInfo } from "@/features/ai";
import { CATEGORY_EMOJI, SEVERITY_EMOJI } from "@/features/ai";
import { normalizeSuggestionExplanation } from "@/features/ai/lib/suggestion-format";
import type { LanguageCode } from "@/shared/types/language";
import { ISSUE_FIELD_LABELS, REPEAT_BADGE_LABELS, VERIFICATION_LABELS } from "@/shared/constants";

type RepeatAnnotatedIssue = StructuredIssue & {
  repeat?: RepeatBadgeInfo | null;
  verifierConfirmed?: boolean;
};

interface ReviewComment {
  path: string;
  line: number;
  startLine?: number;
  body: string;
}

interface PostPRReviewParams {
  token: string;
  owner: string;
  repo: string;
  prNumber: number;
  reviewBody: string;
  suggestions: CodeSuggestion[];
  issues: RepeatAnnotatedIssue[];
  headSha: string;
  langCode: LanguageCode;
}

/**
 * PR Review APIл°Ь мЭЄлЭЉмЭЄ suggestion мљФл©ШнКЄл•Љ нПђмК§нМЕнХЬлЛ§.
 * м†Дм≤і л¶ђлЈ∞ мЪФмХљмЭА review bodyмЧР, к∞Ьл≥Д м†ЬмХИмЭА inline commentл°Ь.
 *
 * вЪ†пЄП лґДл¶ђ нПђмК§нМЕ м†ДлЮµ:
 * inline issuesл•Љ suggestionsмЩА к∞ЩмЭА createReview() нШЄмґЬмЧР лД£мЬЉл©і,
 * AIк∞А diff л≤ФмЬД л∞Ц line numberл•Љ мГЭмД±нХЬ к≤љмЪ∞ м†Дм≤і нШЄмґЬмЭі 422л°Ь мЛ§нМ®нХШмЧђ м†ХмГБ suggestionsкєМмІА мЖРмЛ§лРЬлЛ§.
 * suggestionsлКФ before нХДлУЬл°Ь diff м†ХнХ©мД±мЭД к≤Ам¶ЭнХ† мИШ мЮИмІАлІМ, issuesлКФ к≤Ам¶Э мИШлЛ®мЭі мЧЖлЛ§.
 * лФ∞лЭЉмДЬ suggestions л®Љм†А нПђмК§нМЕ вЖТ issues л≥ДлПД нПђмК§нМЕ(мЛ§нМ® нЧИмЪ©) м†ДлЮµмЭД мВђмЪ©нХЬлЛ§.
 */
export async function postPRReviewWithSuggestions(params: PostPRReviewParams): Promise<void> {
  const { token, owner, repo, prNumber, reviewBody, suggestions, issues, headSha, langCode } = params;
  const labels = ISSUE_FIELD_LABELS[langCode];
  const octokit = createOctokitClient(token);

  // suggestion comments
  const suggestionComments: ReviewComment[] = suggestions.map((s) => {
    const beforeLineCount = s.before.split("\n").length;
    const comment: ReviewComment = {
      path: s.file,
      line: s.line + beforeLineCount - 1,
      body: formatSuggestionComment(s),
    };
    if (beforeLineCount > 1) {
      comment.startLine = s.line;
    }
    return comment;
  });

  // issue comments (file+line лСШ лЛ§ мЮИлКФ issuesлІМ inline commentл°Ь)
  // file-level issues (line: null)лКФ review body нЕМмЭілЄФмЧР нПђнХ®лР®
  // вЪ†пЄП type predicate мВђмЪ© вАФ plain .filter()лКФ TypeScript narrowing лґИк∞А
  const inlineIssues = issues.filter(
    (i): i is RepeatAnnotatedIssue & { file: string; line: number } =>
      i.file !== null && i.line !== null
  );
  const issueComments: ReviewComment[] = inlineIssues.map((i) => ({
    path: i.file,
    line: i.line,
    body: formatIssueComment(i, labels, REPEAT_BADGE_LABELS[langCode], VERIFICATION_LABELS[langCode]),
  }));

  // 1м∞® нШЄмґЬ: suggestions + review body (summary + general issues нЕМмЭілЄФ)
  await octokit.rest.pulls.createReview({
    owner,
    repo,
    pull_number: prNumber,
    commit_id: headSha,
    body: `## AI Code Review\n\n${reviewBody}\n\n---\n*Generated by HReviewer*`,
    event: "COMMENT",
    comments: suggestionComments.map(({ startLine, ...c }) => ({
      ...c,
      ...(startLine ? { start_line: startLine } : {}),
    })),
  });

  // 2м∞® нШЄмґЬ: inline issues вАФ мЛ§нМ®нХілПД suggestionsмЧР мШБнЦ• мЧЖмЭМ
  // вЪ†пЄП line-specific issues(file+line)лКФ review bodyмЧР лѓЄнПђнХ® вАФ 2м∞® нШЄмґЬ мЛ§нМ® мЛЬ мЬ†мЛ§лР®
  // general issues(line: null)лІМ review body нЕМмЭілЄФмЧР нПђнХ®лРШмЦі л≥ім°ілР®
  if (issueComments.length > 0) {
    try {
      await octokit.rest.pulls.createReview({
        owner,
        repo,
        pull_number: prNumber,
        commit_id: headSha,
        // body нХДлУЬ мГЭлЮµ вАФ body: "" мВђмЪ© мЛЬ GitHub PR Conversation нГ≠мЧР лєИ review entry мГЭмД±лР®
        event: "COMMENT",
        // issueCommentsлКФ startLineмЭі мЧЖмЬЉлѓАл°Ь (single-lineлІМ) мІБм†С м†ДлЛђ
        comments: issueComments.map(({ body, path, line }) => ({
          path,
          line,
          body,
        })),
      });
    } catch (error) {
      console.warn("Inline issue comments failed (suggestions were posted successfully):", error);
    }
  }
}

function formatSuggestionComment(suggestion: CodeSuggestion): string {
  const explanation = normalizeSuggestionExplanation(suggestion.explanation);

  return `${SEVERITY_EMOJI[suggestion.severity]} **${suggestion.severity}**: ${explanation}

\`\`\`suggestion
${suggestion.after}
\`\`\``;
}

function formatIssueComment(
  issue: RepeatAnnotatedIssue,
  labels: { impact: string; recommendation: string },
  repeatLabels: { badge: string; context: string },
  verificationLabels: { badge: string },
): string {
  const sev = `${SEVERITY_EMOJI[issue.severity]} ${issue.severity}`;
  const cat = `${CATEGORY_EMOJI[issue.category]} ${issue.category}`;

  // л∞©мЦім†Б кЄ∞л≥Єк∞Т вАФ in-flight resume + лєИ к∞Т лМАмЭС
  const title = (issue.title ?? "").trim();
  const rawBody = (issue.body ?? (issue as { description?: string }).description ?? "").trim();
  const impact = (issue.impact ?? "").trim();
  const recommendation = (issue.recommendation ?? "").trim();

  // лђЄмЮ• к≤љк≥Д к≤АмВђ + body лєИк∞Т skip guard
  const titleSuffix = title && rawBody.startsWith(title) ? rawBody.slice(title.length) : null;
  const body =
    titleSuffix !== null && (titleSuffix === "" || /^[.,:;вАФ\-]/.test(titleSuffix))
      ? titleSuffix.replace(/^[\s.,:;вАФ\-]+/, "")
      : rawBody;

  const lines: string[] = [
    `### ${sev} ¬Ј ${cat}${title ? ` вАФ ${title}` : ""}`,
  ];
  if (issue.repeat) {
    lines.push("", `> вЪ†пЄП **${repeatLabels.badge}** вАФ ${repeatLabels.context} ${issue.repeat.prUrl} (${issue.repeat.date})`);
  }
  if (issue.verifierConfirmed) {
    lines.push("", `> вЬЕ **${verificationLabels.badge}**`);
  }
  if (body) lines.push("", body);
  if (impact) lines.push("", `**${labels.impact}:** ${impact}`);
  if (recommendation) lines.push("", `**${labels.recommendation}:** ${recommendation}`);
  return lines.join("\n");
  // SYNC:formatIssueBody вАФ review-formatter.ts ¬Ј structured-review-body.tsx мЩА лПЩмЭЉ л°ЬмІБ мЬ†мІА
}

/** к≤АмИШмЮР л™ЕмЭШ(лПЩмЭЉ к≥Дм†Х)мЭШ body-only л¶ђлЈ∞ мЧФнКЄл¶ђ к≤МмЛЬ.
 *  мЭЄлЭЉмЭЄ мљФл©ШнКЄ мЧЖмЭМ вАФ bodyк∞А мЮИлКФ reviewлКФ PR Conversation нГ≠мЧР л≥ДлПД л¶ђлЈ∞ мєілУЬл°Ь лВШнГАлВЬлЛ§
 *  (мЬД postPRReviewWithSuggestions 2м∞® нШЄмґЬмЭШ "body нХДлУЬ мГЭлЮµ" м£ЉмДЭк≥Љ лПЩмЭЉ кЈЉк±∞мЭШ мЧ≠л∞©нЦ• нЩЬмЪ©). */
export async function postVerificationReview(params: {
  token: string;
  owner: string;
  repo: string;
  prNumber: number;
  headSha: string;
  body: string;
}): Promise<void> {
  const { token, owner, repo, prNumber, headSha, body } = params;
  const octokit = createOctokitClient(token);
  await octokit.rest.pulls.createReview({
    owner,
    repo,
    pull_number: prNumber,
    commit_id: headSha,
    body,
    event: "COMMENT",
  });
}
