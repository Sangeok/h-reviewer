import { createOctokitClient } from "./github";
import type { CodeSuggestion, StructuredIssue } from "@/module/ai";
import { CATEGORY_EMOJI, SEVERITY_EMOJI } from "@/module/ai";

interface ReviewComment {
  path: string;
  line: number;
  startLine?: number;
  body: string;
}

interface PostPRReviewParams {
  token: string;
  owner: string;
  repo: string;
  prNumber: number;
  reviewBody: string;
  suggestions: CodeSuggestion[];
  issues: StructuredIssue[];
  headSha: string;
}

/**
 * PR Review APIлЎң мқёлқјмқё suggestion мҪ”л©ҳнҠёлҘј нҸ¬мҠӨнҢ…н•ңлӢӨ.
 * м „мІҙ лҰ¬л·° мҡ”м•ҪмқҖ review bodyм—җ, к°ңлі„ м ңм•ҲмқҖ inline commentлЎң.
 *
 * вҡ пёҸ л¶„лҰ¬ нҸ¬мҠӨнҢ… м „лһө:
 * inline issuesлҘј suggestionsмҷҖ к°ҷмқҖ createReview() нҳём¶ңм—җ л„Јмңјл©ҙ,
 * AIк°Җ diff лІ”мң„ л°– line numberлҘј мғқм„ұн•ң кІҪмҡ° м „мІҙ нҳём¶ңмқҙ 422лЎң мӢӨнҢЁн•ҳм—¬ м •мғҒ suggestionsк№Ңм§Җ мҶҗмӢӨлҗңлӢӨ.
 * suggestionsлҠ” before н•„л“ңлЎң diff м •н•©м„ұмқ„ кІҖмҰқн•  мҲҳ мһҲм§Җл§Ң, issuesлҠ” кІҖмҰқ мҲҳлӢЁмқҙ м—ҶлӢӨ.
 * л”°лқјм„ң suggestions лЁјм Җ нҸ¬мҠӨнҢ… вҶ’ issues лі„лҸ„ нҸ¬мҠӨнҢ…(мӢӨнҢЁ н—Ҳмҡ©) м „лһөмқ„ мӮ¬мҡ©н•ңлӢӨ.
 */
export async function postPRReviewWithSuggestions(params: PostPRReviewParams): Promise<void> {
  const { token, owner, repo, prNumber, reviewBody, suggestions, issues, headSha } = params;
  const octokit = createOctokitClient(token);

  // suggestion comments
  const suggestionComments: ReviewComment[] = suggestions.map((s) => {
    const beforeLineCount = s.before.split("\n").length;
    const comment: ReviewComment = {
      path: s.file,
      line: s.line + beforeLineCount - 1,
      body: formatSuggestionComment(s),
    };
    if (beforeLineCount > 1) {
      comment.startLine = s.line;
    }
    return comment;
  });

  // issue comments (file+line л‘ҳ лӢӨ мһҲлҠ” issuesл§Ң inline commentлЎң)
  // file-level issues (line: null)лҠ” review body н…Ңмқҙлё”м—җ нҸ¬н•ЁлҗЁ
  // вҡ пёҸ type predicate мӮ¬мҡ© вҖ” plain .filter()лҠ” TypeScript narrowing л¶Ҳк°Җ
  const inlineIssues = issues.filter(
    (i): i is StructuredIssue & { file: string; line: number } =>
      i.file !== null && i.line !== null
  );
  const issueComments: ReviewComment[] = inlineIssues.map((i) => ({
    path: i.file,
    line: i.line,
    body: formatIssueComment(i),
  }));

  // 1м°Ё нҳём¶ң: suggestions + review body (summary + general issues н…Ңмқҙлё”)
  await octokit.rest.pulls.createReview({
    owner,
    repo,
    pull_number: prNumber,
    commit_id: headSha,
    body: `## AI Code Review\n\n${reviewBody}\n\n---\n*Generated by HReviewer*`,
    event: "COMMENT",
    comments: suggestionComments.map(({ startLine, ...c }) => ({
      ...c,
      ...(startLine ? { start_line: startLine } : {}),
    })),
  });

  // 2м°Ё нҳём¶ң: inline issues вҖ” мӢӨнҢЁн•ҙлҸ„ suggestionsм—җ мҳҒн–Ҙ м—ҶмқҢ
  // вҡ пёҸ line-specific issues(file+line)лҠ” review bodyм—җ лҜёнҸ¬н•Ё вҖ” 2м°Ё нҳём¶ң мӢӨнҢЁ мӢң мң мӢӨлҗЁ
  // general issues(line: null)л§Ң review body н…Ңмқҙлё”м—җ нҸ¬н•Ёлҗҳм–ҙ ліҙмЎҙлҗЁ
  if (issueComments.length > 0) {
    try {
      await octokit.rest.pulls.createReview({
        owner,
        repo,
        pull_number: prNumber,
        commit_id: headSha,
        // body н•„л“ң мғқлһө вҖ” body: "" мӮ¬мҡ© мӢң GitHub PR Conversation нғӯм—җ л№Ҳ review entry мғқм„ұлҗЁ
        event: "COMMENT",
        // issueCommentsлҠ” startLineмқҙ м—ҶмңјлҜҖлЎң (single-lineл§Ң) м§Ғм ‘ м „лӢ¬
        comments: issueComments.map(({ body, path, line }) => ({
          path,
          line,
          body,
        })),
      });
    } catch (error) {
      console.warn("Inline issue comments failed (suggestions were posted successfully):", error);
    }
  }
}

function formatSuggestionComment(suggestion: CodeSuggestion): string {
  return `${SEVERITY_EMOJI[suggestion.severity]} **${suggestion.severity}**: ${suggestion.explanation}

\`\`\`suggestion
${suggestion.after}
\`\`\``;
}

function formatIssueComment(issue: StructuredIssue): string {
  return `${CATEGORY_EMOJI[issue.category]} ${SEVERITY_EMOJI[issue.severity]} **${issue.severity}** | ${issue.category}\n\n${issue.description}`;
}
